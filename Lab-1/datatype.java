class datatype
{
	public static void main(String args[])
	{
		int a=19;
		float c=78.56f;
		byte b=56;
		String s="Hello, this is a use of String datatype.";
		boolean t=true;
		char h='h';
		long l=89898989L;

		System.out.println("Value of a of type int is "+a);
		System.out.println("Value of b of type byte is "+b);
		System.out.println("Value of c of type float is "+c);
		System.out.println("Value of s of type String is "+s);	
		System.out.println("Value of h of type char is "+h);
		System.out.println("Value of t of type boolean is "+t);
		System.out.println("Value of l of type long is "+l);
	}
}

